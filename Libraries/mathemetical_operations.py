def add(a, b):
    """
    Addition is the most basic operation of arithmetic. In its simple form,
    addition combines two numbers, the addends or terms, into a single number,
    the sum of the numbers (Such as 2 + 2 = 4 or 3 + 5 = 8).
    """
    return a + b

def substract(a, b):
    """
    Subtraction is the inverse operation to addition. Subtraction finds the
    difference between two numbers, the minuend minus the subtrahend
    (Such as 4 - 2 = 2 or 8 - 5 = 3).
    """
    return a - b

def divide(a, b):
    """
    Division finds the quotient of two numbers, the dividend divided by the
    divisor. Any dividend divided by 0 is undefined. For distinct positive
    numbers, if the dividend is larger than the divisor, the quotient is
    greater than 1, otherwise it is less than 1 (a similar rule applies for
    negative numbers). (Such as 4 / 2 = 2 or 6 / 2 = 3).
    """
    return a / b

def multiply(a, b):
    """
    Multiplication is the second basic operation of arithmetic.
    Multiplication also combines two numbers into a single number,
    the product (Such as 4 * 2 = 8 or 6 * 2 = 12).
    """
    return a * b
